"""
Calculator library containing basic math operations.
"""


def add(first_term, second_term):
    """Add operator

    Args:
        first_term ([int]):
        second_term ([int]):

    Returns:
        [int]:
    """
    return first_term + second_term


def subtract(first_term, second_term):
    """Subtract operator

    Args:
        first_term (int):
        second_term (int):

    Returns:
        [int]:
    """
    return first_term - second_term
